export function inferSeverity(question: string): "Crítica" | "Maior" | "Menor" {
  const q = question.toLowerCase();
  if (q.includes("higieniz") || q.includes("esteriliz") || q.includes("precaução") || q.includes("epi") || q.includes("invasiv")) return "Crítica";
  if (q.includes("limpeza") || q.includes("resíduo") || q.includes("equipamento") || q.includes("registro")) return "Maior";
  return "Menor";
}

export function getSectorList() {
  return Object.entries(CHECKLISTS_DATA).map(([key, value]) => ({
    key,
    nome: value.nome,
    icon: value.icon,
    totalItems: value.grupos.reduce((acc, g) => acc + g.itens.length, 0),
  }));
}
